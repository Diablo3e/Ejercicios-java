function imprimirArreglo(n){
    for (let number_words of n){

        console.log(number_words);
    }//el FOR....OF  funciona para situaciones mas simples, cuando no se necesita recorrer indices.
    //  ya que en el for normal puedes manejar indices, puede saltar, elegir, etc. mucho mas complejo.
}
console.log('Fourth exercise');
